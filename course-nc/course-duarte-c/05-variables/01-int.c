
#include <stdio.h>

int main() {

    // Integer
    // By printer using %d
    
    int day;
    day = 29;
    printf("Day: %d \n", day);

    int month = 05;
    printf("Month: %d \n", month);

    int year = -79;
    year = 1979;
    printf("Year: %d \n", year);

    printf("Date: %d-%d-%d", day, month, year);

    return 0;

}