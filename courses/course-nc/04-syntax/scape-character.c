#include <stdio.h>

int main(void) {

  // \n: Nueva línea (salto de línea).

  // \t: Tabulación horizontal.

  // \r: Retorno de carro.

  // \\: Barra invertida (backslash).

  // \": Comilla doble.

  // \': Comilla simple.

  // \a: Alerta (sonido de campana).

  // \b: Retroceso.

  // \f: Avance de página.

  // \v: Tabulación vertical.

  // \?: Carácter de interrogación.
  
  printf("Salto de linea \n");
  printf("Pitido \a");
  printf("Retorno de caroo \r");

  return 0;

}
